    fun searchWords(query: String): List<FulaniWord> {
        return allWords.filter { 
            it.word.contains(query, ignoreCase = true) ||
            it.pronunciation.contains(query, ignoreCase = true) ||
            it.meaning.contains(query, ignoreCase = true)
        }
    }
    
